// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     *write_data     魹ｽ魹ｽ魹ｽﾍｵ魹ｽ魹ｽ魹ｽ魹ｽﾝｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｷ

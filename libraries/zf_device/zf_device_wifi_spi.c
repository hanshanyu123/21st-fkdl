// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     *buffer         魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｷ
// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     length          魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     wait_time       魹ｽ魹ｽ魹ｽﾈｴ魹ｽﾊｱ魹ｽ魹ｽ 魹ｽ魹ｽﾎｻ100ﾎ｢魹ｽ魹ｽ
// 魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ     uint8           ﾗｴﾌｬ 0-魹ｽﾉｹ魹ｽ 1-魹ｽ魹ｽ魹ｽ魹ｽ
// ﾊｹ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ     魹ｽﾚｲ魹ｽﾊｹ魹ｽﾃ｣魹ｽ魹ｽﾃｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽﾗ｢魹ｽ魹ｽﾏ｢
//-------------------------------------------------------------------------------------------------------------------
static uint8 wifi_spi_set_parameter (wifi_spi_packets_command_enum command, uint8 *buffer, uint16 length, uint32 wait_time)
{
    uint8 return_state;
    wifi_spi_head_struct head;
    return_state = 1;
    do
    {
        head.command = command;
        head.length  = length;
        
        // 魹ｽﾈｴ魹ｽ魹ｽﾓｻ魹ｽﾗｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
        if(wifi_spi_wait_idle(wait_time))
        {
            break;
        }

        wifi_spi_write(&head.command, sizeof(wifi_spi_head_struct), buffer, length);
        if(wifi_spi_wait_idle(wait_time))
        {
            break;
        }
        // 魹ｽ魹ｽ魹ｽ魹ｽﾓｦ魹ｽ魹ｽ魹ｽﾅｺ魹ｽ

        head.command = WIFI_SPI_DATA;
        head.length = 0;
        wifi_spi_transfer_command((wifi_spi_packets_struct *)&head, head.length);
        system_delay_us(20);
        if(WIFI_SPI_REPLY_OK == head.command)
        {
            return_state = 0;
        }
    }while(0);
    
    return return_state;
}

